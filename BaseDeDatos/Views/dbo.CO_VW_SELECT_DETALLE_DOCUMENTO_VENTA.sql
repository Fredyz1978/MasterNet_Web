CREATE VIEW [dbo].[CO_VW_SELECT_DETALLE_DOCUMENTO_VENTA]
AS
SELECT			doc.CO_DOC_VEN_NUM,
				det.*, art.*, 
                ice.CO_ICE_NOM ,
                ice.CO_ICE_POR,
				ice.CO_ICE_COD_PORC_SRI
FROM            dbo.CO_DETALLE_DOCUMENTO_VENTA det INNER JOIN
				dbo.CO_DOCUMENTO_VENTA doc ON det.CO_DOC_VEN_ID = doc.CO_DOC_VEN_ID INNER JOIN
				dbo.CO_ARTICULO art ON det.CO_DET_DOC_VEN_ART_COD = art.CO_ART_COD 
					AND doc.CO_EMP_RUC = art.CO_EMP_RUC LEFT OUTER JOIN
				dbo.CO_TABLA_ICE ice ON ice.CO_ICE_COD = art.CO_ICE_COD
