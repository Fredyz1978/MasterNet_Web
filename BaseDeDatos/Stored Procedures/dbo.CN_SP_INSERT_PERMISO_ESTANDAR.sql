CREATE PROCEDURE [dbo].[CN_SP_INSERT_PERMISO_ESTANDAR]
@CO_GRU_COD int,
@USUA_COD INT
as
INSERT INTO PERMISO
(PERM_NOM,
CONT_COD,
USUA_COD,
CO_GRU_COD)

SELECT 
NOMBRE
,0
,@USUA_COD
,@CO_GRU_COD
FROM CO_MENU
WHERE NOMBRE NOT IN('Manejo','Procesos','Reportes')
and DISPONIBLE=1
GO
