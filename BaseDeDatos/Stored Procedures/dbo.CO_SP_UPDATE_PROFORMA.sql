UPDATE [dbo].[CO_PROFORMA] SET
[CO_CLI_COD]=@CO_CLI_COD
,[CO_VEN_CED]=@CO_VEN_CED
,[CO_ALM_COD]=@CO_ALM_COD
,[CO_PRO_FEC]=@CO_PRO_FEC
,[CO_PRO_EST]=@CO_PRO_EST
,[CO_PRO_DET]=@CO_PRO_DET
,[CO_PRO_TOT_NET]=@CO_PRO_TOT_NET
