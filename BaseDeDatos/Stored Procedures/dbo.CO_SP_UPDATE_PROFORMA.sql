,@CO_PRO_CHE_VAL float
,@CO_PRO_CHE_NUM nvarchar(20)
,@CO_PRO_CHE_BAN nvarchar(40)
,@CO_PRO_TAR bit
,@CO_PRO_TAR_VAL float
,@CO_PRO_TAR_NUM nvarchar(30)
,@CO_PRO_TAR_VAU nvarchar(50)
,@CO_PRO_NUM_PAG int
,@CO_PRO_USU_ALI nvarchar(15)
,@CO_PRO_TOT_LET nvarchar(150)
,@CO_PEDIDO_VENDEDOR varchar(10)
