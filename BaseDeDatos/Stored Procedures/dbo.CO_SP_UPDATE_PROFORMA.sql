CREATE PROC [dbo].[CO_SP_UPDATE_PROFORMA]
@CO_PRO_COD int
,@CO_EMP_RUC char(13)
,@CO_CLI_COD nvarchar(50)
,@CO_VEN_CED nchar(10)
,@CO_ALM_COD int
,@CO_PRO_FEC datetime
,@CO_PRO_EST nvarchar(50)
,@CO_PRO_DET nvarchar(100)
,@CO_PRO_TOT_NET float
,@CO_PRO_POR_DES float
,@CO_PRO_DES float
,@CO_PRO_POR_IVA int
